    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String authorName = (String) cmbAuthor.getSelectedItem();
        String bookName = txtName.getText().trim();
        String numOfPageStr = txtNumOfPage.getText().trim();
        String language = txtLanguage.getText().trim();
        String priceStr = txtPrice.getText().trim();

        if (authorName == null || bookName.isEmpty() || numOfPageStr.isEmpty() || language.isEmpty() || priceStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all fields!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        short numOfPage;
        try {
            numOfPage = Short.parseShort(numOfPageStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Number of Pages must be a number!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Date registeredDate = new Date();

        // find author from AuthorDirectory
        Author author = authorList.findAuthorByName(authorName);
        if (author == null) {
            JOptionPane.showMessageDialog(this, "Author not found!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        double price;
        try {
            price = Double.parseDouble(priceStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Price must be a number!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        Book newBook = allBooks.addNewBook(bookName, registeredDate, selectedLib, author, price);
        newBook.setNumberOfPages(numOfPage);
        newBook.setLanguage(language);

        JOptionPane.showMessageDialog(this, "Book added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

        // reset
        cmbAuthor.setSelectedIndex(-1);
        txtName.setText("");
        txtNumOfPage.setText("");
        txtLanguage.setText("");
        txtLibrary.setText("");
    }//GEN-LAST:event_btnAddActionPerformed


