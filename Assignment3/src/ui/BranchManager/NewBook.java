    private void populateAuthorComboBox() {
        cmbAuthor.removeAllItems();

        for (Author author : authorList.getAllAuthors()) {
            cmbAuthor.addItem(author.getName());
        }

        cmbAuthor.setSelectedIndex(-1);
    }
