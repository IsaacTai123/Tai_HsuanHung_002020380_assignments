    private void populateBranchManagerComboBox() {
        cmbBranchManager.removeAllItems();
        boolean hasBranchManager = false;

        for (IUserProfile u : userList.getAllUsers()) {
            if (u instanceof BranchManager) {
                cmbBranchManager.addItem(u.getName());
                hasBranchManager = true;
            }
        }

        cmbBranchManager.setSelectedIndex(-1);

        if (!hasBranchManager) {
            JOptionPane.showMessageDialog(this, "Please create a Branch Manager user first!", "No Branch Manager Available", JOptionPane.WARNING_MESSAGE);
        }
    }
