    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String libraryName = txtLibraryName.getText().trim();
        String buildingNoStr = txtBuildingNo.getText().trim();
        String selectedManagerStr = (String) cmbBranchManager.getSelectedItem();
        String experience = txtExperience.getText().trim();
        
        if (selectedManagerStr == null || selectedManagerStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please create a Branch Manager user first!", "No Branch Manager Available", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if ( libraryName.isEmpty() || buildingNoStr.isEmpty() || experience.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please complete all fields!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int buildingNo;
        try {
            buildingNo = Integer.parseInt(buildingNoStr);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Building number must be a valid integer!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        IUserProfile user = userList.findUserByName(selectedManagerStr);
        if (!(user instanceof BranchManager)) {
            JOptionPane.showMessageDialog(this, "Selected user is not a Branch Manager!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        emList.addBranchManager((BranchManager) user);
        libraryList.addLibrary(libraryName, buildingNo, (BranchManager) user);
        

        JOptionPane.showMessageDialog(this, "User created successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

        // Reset fields
        txtLibraryName.setText("");
        txtBuildingNo.setText("");
        cmbBranchManager.setSelectedIndex(-1);
        txtEmployeeID.setText("");
        txtExperience.setText("");
    }//GEN-LAST:event_btnAddActionPerformed


